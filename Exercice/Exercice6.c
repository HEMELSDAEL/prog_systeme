#include <stdio.h>

int main(){
  int nombre1, nombre2;
  printf("Veuillez entrer votre premier nombre :\n");
  scanf("%d", &nombre1);
  printf("Veuillez entrer votre second nombre :\n");
  scanf("%d", &nombre2);
  printf("---------permutation------------");
  printf("Le premier nombre est :%d\n", nombre2);
  printf("Le second nombre est :%d\n", nombre1);
  return 0;
}
